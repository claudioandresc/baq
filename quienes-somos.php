<p style='margin-bottom:0; font-style:italic;'>“Poetizar es propiamente dejar habitar. Ahora bien, ¿por qué medio llegamos a tener un habitáculo? Por medio del edificar. Poetizar, como dejar habitar, es un construir.”</p>
<p style='margin-top:0; text-align:right; font-weight:bold;'>“…Poéticamente habita el hombre…”<br /><i>Martin Heidegger</i></p>

<p>Esta corta y concisa frase traduce de manera sintética nuestra visión.  Por un lado se refiere al hecho poético como al dinámica  que construye la fantasía, del sueño; sin embargo advierte la necesidad de construir para consumar este hecho. </p>

<p>Pero “construir” no se traduce en el mero hecho de edificar; esta acción abarca mucho más. El hombre construye relaciones, que se consuman y mantiene  en el tiempo. En tal sentido construir es  el sentido básico y esencial del habitar, es a la vez sembrar y cosechar el fruto del cotidiano vivir (poner la mesa, sembrar el jardín, el olor del café de la mañana). Así se genera un nexo habitual compuesto de pequeños gestos que nos ligan a nuestro entorno, pensamos en como habitamos, porque sabemos cómo cuidamos, conservamos, y en consecuencia como construimos.</p>

<p>El construir a partir de pequeños gestos convierte al diseño en la suma de consistentes detalles que arman el carácter de la placida belleza del paisaje cotidiano.  Es allí donde la correcta proyección de las ideas, se conecta con personas, su memoria, objetos y medio natural, que permiten la consolidación de una poética del espacio.</p> 

<p>Esta poética se convierte en nuestra visión; otorgar a nuestros clientes proyectos  que en el marco del rescate de su cultura e intereses, contribuyan en la construcción de recuerdos, amables suvenires  hechos  espacios que conmuevan en lo individual y lo social.</p>
